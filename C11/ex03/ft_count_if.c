int	ft_count_if(char **tab, int length, int (*f)(char*))
{
	int	i;
	int	cmp;

	i = 0;
	cmp = 0;
	while (i < length)
	{
		if ((*f)(tab[i]))
			cmp++;
		i++;
	}
	return (cmp);
}
